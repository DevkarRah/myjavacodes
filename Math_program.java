package java_programs;

public class Math_program {
	public static void main(String[] args) 
	{
	
		Math.addExact(100, 200);
		System.out.println(Math.addExact(100, 200));
		System.out.println(Math.subtractExact(800, 500) );
		System.out.println(Math.multiplyExact(100, 200)  );
		System.out.println(Math.max(2.44, 10)  );
		System.out.println(Math.max(4.55, 5.666)  );
		System.out.println(Math.min(12, 45)  );
		System.out.println(Math.sqrt(7)  );
		System.out.println(Math.PI  );
		
		System.out.println(Math.random() );
	}

}
